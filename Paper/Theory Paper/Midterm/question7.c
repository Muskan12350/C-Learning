#include<stdio.h>

int main(){
    int num1, num2, num3=2;

    // Assignment Operators: Used to assign values to variables (e.g: =, +=, -=, *=, /=)
    num1 = 5;
    num2 = 8;
    num3+=2;

    // Equality Operator: Compares the equality between two operands
    if(num1==num2){
        printf("Numbers are equal");
    }

    return 0;
}
