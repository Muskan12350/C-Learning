#include <stdio.h>

int main(){
    // Escape Sequence for beep and new line
    printf("This is a \a beep\n");               // \n will change the line

    // Escape Sequence for backspace
    printf("This is a\b backspace\n");

    // Escape Sequence for tab space
    printf("This is a \t tab space\n");

    // Escape Sequence for backslash
    printf("This is a \\ backslash\n");

    // Escape Sequence for single quotes
    printf("These are \'Single Quotes\' \n");

    // Escape Sequence for double quotes
    printf("These are \"Double Quotes\" \n");

    // Escape Sequence for question mark
    printf("This is a \? question mark\n");

    return 0;
}
